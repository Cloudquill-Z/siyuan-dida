export type TranslationKey = keyof (typeof translations)["en"];
export type Translate = (key: TranslationKey, values?: Record<string, string | number>) => string;

export function createTranslator(locale = "en"): Translate {
  const dictionary = locale.toLowerCase().startsWith("zh") ? translations["zh-CN"] : translations.en;
  return (key, values = {}) =>
    dictionary[key].replace(/\{(\w+)\}/g, (_, name: string) => String(values[name] ?? `{${name}}`));
}
